-- 問題の再現（二重購入による在庫マイナス）
-- ============================================================
-- 接続: psql -h localhost -U udemart -d udemart
-- 事前準備: setup.sql を先に実行してください
-- ============================================================

-- 【注意】このセクションは2つのターミナルでpsqlを開いて実行します
--
--   ターミナルA: psql -h localhost -U udemart -d udemart
--   ターミナルB: psql -h localhost -U udemart -d udemart
--
-- まず在庫を1に戻しておく（片方のターミナルで実行）
-- ============================================================

UPDATE inventory SET quantity = 1 WHERE product_id = 1;
SELECT quantity FROM inventory WHERE product_id = 1;
-- → quantity = 1 であることを確認

-- ------------------------------------------------------------
-- 【ターミナルA でここまで実行する】
-- ------------------------------------------------------------
BEGIN;
SELECT quantity FROM inventory WHERE product_id = 1;
-- → 1 と表示される。「まだ在庫がある！」と判断する

-- ここで一旦手を止め、ターミナルBに切り替える
-- （実際の本番環境では、ここでアプリが少し処理に時間がかかるイメージ）

-- ------------------------------------------------------------
-- 【ターミナルB でここまで実行する】
-- ------------------------------------------------------------
-- BEGIN;
-- SELECT quantity FROM inventory WHERE product_id = 1;
-- → 1 と表示される
--   ターミナルAがまだ UPDATE していないので、同じく「在庫あり」と判断してしまう！
--   これが問題の根源：2人が同時に「1個ある」と読んでしまった

-- ------------------------------------------------------------
-- 【ターミナルA でここを実行する】
-- ------------------------------------------------------------
UPDATE inventory
   SET quantity = quantity - 1
 WHERE product_id = 1;
COMMIT;
-- → quantity = 0 になる（顧客Aさんの購入が完了）

-- ------------------------------------------------------------
-- 【ターミナルB でここを実行する】
-- ------------------------------------------------------------
-- UPDATE inventory
--    SET quantity = quantity - 1
--  WHERE product_id = 1;
-- COMMIT;
-- → quantity = -1 になってしまった！！
--   顧客Bさんにも「購入完了」が表示されたが在庫はマイナスに…

-- 結果確認（どちらかのターミナルで）
SELECT quantity FROM inventory WHERE product_id = 1;
-- → -1


