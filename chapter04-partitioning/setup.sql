-- ------------------------------------------------------------
-- 既存データをリセット
-- ------------------------------------------------------------
DROP TABLE IF EXISTS orders_partitioned CASCADE;

TRUNCATE order_items, orders, inventory, products, categories, customers RESTART IDENTITY CASCADE;

-- ------------------------------------------------------------
-- カテゴリ（最小限）
-- ------------------------------------------------------------
INSERT INTO categories (name) VALUES
    ('電子機器'),
    ('その他');

-- ------------------------------------------------------------
-- 商品（3件）
-- ------------------------------------------------------------
INSERT INTO products (name, category_id, price) VALUES
    ('スマートフォン', 1, 89800.00),
    ('タブレット',     1, 59800.00),
    ('ノートPC',       1, 128000.00);

-- ------------------------------------------------------------
-- 顧客（10,000件）
-- generate_series で連番を生成し、都道府県をラウンドロビンで割り当て
-- ------------------------------------------------------------
INSERT INTO customers (name, email, prefecture)
SELECT
    '顧客_' || g,
    'user_' || g || '@example.com',
    (ARRAY['東京都', '大阪府', '神奈川県'])[(g % 3) + 1]
FROM generate_series(1, 10000) g;

-- ------------------------------------------------------------
-- 在庫（商品ごとの初期在庫）
-- ------------------------------------------------------------
INSERT INTO inventory (product_id, quantity) VALUES
    (1, 1000),
    (2, 1000),
    (3,  500);

-- ------------------------------------------------------------
-- 注文（500,000件、2022年〜2024年分）
--
-- ★ このデータ量が「テーブルが巨大化して月次レポートが遅くなる」
--   状況を再現するためのポイントです。
--
-- 生成ロジック:
--   - customer_id: 1〜10000 からランダム
--   - status: 5種類からランダム
--   - total_amount: 1,000〜201,000 円のランダムな金額
--   - ordered_at: 2022-01-01 から最大 1095 日（約3年）後のランダムな日時
-- ------------------------------------------------------------
INSERT INTO orders (customer_id, status, total_amount, ordered_at)
SELECT
    ((random() * 9999) + 1)::INTEGER AS customer_id,
    (ARRAY['pending', 'confirmed', 'shipped', 'delivered', 'cancelled'])
        [(random() * 4 + 1)::INTEGER] AS status,
    ((random() * 200000) + 1000)::NUMERIC(12, 2) AS total_amount,
    -- 2022年〜2024年の範囲でランダムな日時を生成
    TIMESTAMP '2022-01-01'
        + ((random() * 1095)::INTEGER || ' days')::INTERVAL
        + ((random() * 86400)::INTEGER || ' seconds')::INTERVAL AS ordered_at
FROM generate_series(1, 500000);

-- ------------------------------------------------------------
-- 統計情報を更新（EXPLAIN ANALYZE の精度を上げるため）
-- ------------------------------------------------------------
ANALYZE orders;

-- ------------------------------------------------------------
-- 投入結果の確認
-- ------------------------------------------------------------
SELECT
    '注文件数: ' || count(*) ||
    '、期間: ' || min(ordered_at)::DATE || ' 〜 ' || max(ordered_at)::DATE AS summary
FROM orders;
